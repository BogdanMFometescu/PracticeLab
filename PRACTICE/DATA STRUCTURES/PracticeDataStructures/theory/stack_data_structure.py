# A stack works on the principle of LIFO - Last IN First OUT
# We can use the 'pop' function to remove the last element and return it in a variable

# Create a stack
my_stack = [1,2,3]

# Remove the last element from the stack using  'pop'  function.

removed_item = my_stack.pop()

# The removed element
print(removed_item)

# The stack without the last element
print(my_stack)
